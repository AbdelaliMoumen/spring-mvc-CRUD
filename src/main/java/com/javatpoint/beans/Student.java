package com.javatpoint.beans;  
  
public class Student {  
private int id;  
private String name;  
private float note;  

  
public int getId() {  
    return id;  
}  
public void setId(int id) {  
    this.id = id;  
}  
public String getName() {  
    return name;  
}  
public void setName(String name) {  
    this.name = name;  
}  
public float getNote() {  
    return note;  
}  
public void setNote(float note) {  
    this.note = note;  
}  

  
}  